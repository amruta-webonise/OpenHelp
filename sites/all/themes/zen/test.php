        <p>The internet can often feel like an endless source of distraction and interrupted thoughts. An act as simple
            as watching a video from start to finish can turn into a Herculean task. Why not hide the clutter on your
            screen and rid yourself of distractions? The JW Box, a lightbox designed specifically for the JW Player,
            expands your video (or image) and centers it against a darkened background for an optimal viewing
            experience.</p>

        <p>In this tutorial you will learn how to:</p>
        <ul>
            <li>Download and Install the JW Box</li>
            <li>Configure the JW Box</li>
            <li>Customize the JW Box</li>
        </ul>
