        <h3>CSS Style sheet</h3>

        <p>Truly advanced JW Box users may want to alter the background color of the transparent overlay or the lightbox
            itself. This can be done by editing the <strong>jwbox.css</strong> style sheet directly.</p>

        <h2 id="anatomy"><a name="anatomy_of_a_jw_box"></a>Anatomy of the JW Box</h2><pre>&lt;div class="jwbox"&gt;
<strong> </strong><span style="background-color: rgb(255, 255, 0);">[Default display HTML]
</span>	&lt;div class="jwbox_hidden"&gt;
		&lt;div  class="jwbox_content"&gt;
			<span style="background-color: rgb(255, 255, 0);">[Lightboxed HTML]
</span>		&lt;/div&gt;
	&lt;/div&gt;
&lt;/div&gt;
</pre>
        <p>The JW Box contains the following elements:</p>
        <ol>
            <li>The outer <em>&lt;div "jwbox"&gt;</em>. This serves as a container and reference point for any given JW
                Box.
            </li>
            <li>The HTML which is displayed by default. This is what the user will click on to display the lightboxed
                content.
            </li>
            <li>The <em>&lt;div "jwbox_hidden"&gt;.</em> Everything inside of <em>&lt;div "jwbox_hidden"&gt;</em> is
                hidden until the user clicks on the default HTML, at which point, this content is show. This simple
                principle is what makes it possible to place any HTML content inside of the JW Box.
            </li>
            <li>The <em>&lt;div "jwbox_content"&gt;</em>. Everything outside of <em>&lt;div "jwbox_content"&gt;</em>,
                but within <em>&lt;div "jwbox_hidden"&gt;</em> can be considered part of the close button.
            </li>
        </ol>
